// There is a very intimate relation between vectors and slices:
fn dump(arr: &[i32]) {
    println!("arr is {:?}", arr); // arr is [10, 20, 30]
}

fn main() {
    let mut v = Vec::new();
    v.push(10);
    v.push(20);
    v.push(30);

    dump(&v);

    let slice = &v[1..];
    println!("slice is {:?}", slice); // slice is [20, 30]















































































































































































































































































































































































































    
}